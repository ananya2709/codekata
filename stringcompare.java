import java.io.*;
import java.util.*;
public class Main
{
	public static void main(String[] args) {
		String s,a;
		Scanner sc=new Scanner(System.in);
		s=sc.nextLine();
		a=sc.nextLine();
		int l1,l2;
		l1=s.length();
		l2=a.length();
		if(l1>l2)
		System.out.println(s);
		else if(l1<l2)
		System.out.println(a);
		else
		System.out.println(a);
		}
		
	
}
